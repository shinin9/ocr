import tensorflow as tf
from matplotlib import pyplot as plt
from tqdm import tqdm


mnist = tf.keras.datasets.mnist

(x_train, y_train), (x_test, y_test) = mnist.load_data()
x_train, x_test = x_train / 255.0, x_test / 255.0


path = '/home/dfx/Desktop/yujeong/Project_OCR/easyOCRtest/deep-text-recognition-benchmark/data_mnist/'
gt_train = open('/home/dfx/Desktop/yujeong/Project_OCR/easyOCRtest/deep-text-recognition-benchmark/data_mnist/gt_train.txt', 'a')
gt_test = open('/home/dfx/Desktop/yujeong/Project_OCR/easyOCRtest/deep-text-recognition-benchmark/data_mnist/gt_test.txt', 'a')
gt_val = open('/home/dfx/Desktop/yujeong/Project_OCR/easyOCRtest/deep-text-recognition-benchmark/data_mnist/gt_val.txt', 'a')


for i in tqdm(range(len(x_train))):
    if i <= 50000:
        img = x_train[i].reshape(28,28)
        img = 255-img
        plt.imsave(path+'train/mnist_{:05}.jpg'.format(i+1), img, cmap='gray')
        gt_train.write('train/mnist_{:05}.jpg\t{}\n'.format(i+1, y_train[i]))

    else:
        img = x_train[i].reshape(28,28)
        img = 255-img
        plt.imsave(path+'val/mnist_{:05}.jpg'.format(i+1), img, cmap='gray')
        gt_val.write('val/mnist_{:05}.jpg\t{}\n'.format(i+1, y_train[i]))

for i in tqdm(range(len(x_test))):
    img = x_test[i].reshape(28,28)
    img = 255-img
    plt.imsave(path+'test/mnist_{:05}.jpg'.format(i+1), img, cmap='gray')
    gt_test.write('test/mnist_{:05}.jpg\t{}\n'.format(i+1, y_test[i]))

gt_train.close()
gt_test.close()
gt_val.close()





